// The puzzle after the current one in display order (catalogue / admin order),
// or null if this is the last one.
function nextPuzzleName() {
  const i = game.order.indexOf(game.puzzleName);
  return i >= 0 && i + 1 < game.order.length ? game.order[i + 1] : null;
}

// Build and start a fresh board for the named puzzle, opening a new tracked
// attempt and abandoning any still-open one. The single entry point used by
// boot, "Next puzzle" and a fresh Retry.
async function startLevel(name) {
  finalizeAttempt("abandoned"); // close a prior open attempt (old movesUsed) first
  game.puzzleName = name;
  const gen = ++game.levelGen;
  hideOverlay();

  // One request serves two purposes: derive the puzzle config (seed + params)
  // and pre-load world-best so beginAttempt can show it without a second fetch.
  // Falls back to the deterministic catalogue (cold-start cache) when the server
  // is briefly unreachable.
  const puzzleInfo = await api.getPuzzle(name);
  // A newer startLevel took over while this request was in flight: bail before
  // building, or this stale continuation would render board A while attempts
  // are reported against the newer game.puzzleName.
  if (gen !== game.levelGen) return;
  const cached = catalogByName(name);
  const config = (puzzleInfo && puzzleInfo.seed != null)
    ? { seed: puzzleInfo.seed, numCubes: puzzleInfo.numCubes, scramble: puzzleInfo.scramble, par: puzzleInfo.par }
    : (cached
        ? { seed: cached.seed, numCubes: cached.numCubes, scramble: cached.scramble, par: cached.par }
        : null);
  if (!config) { goToLevels(); return; } // unknown puzzle name — bail to the picker

  game.puzzleConfig = config;
  buildLevel(config);
  game.state = "playing";
  syncWinColorPicker();
  syncUrl(name);
  beginAttempt(puzzleInfo);
}

// Enter a puzzle chosen from the landing page / URL: a clean run (bonus reset).
function enterLevel(name) {
  game.bonus = 0;
  startLevel(name);
}

// Advance after a win to the next puzzle in order, carrying the banked bonus.
// If there is no next puzzle, return to the picker.
function nextLevel() {
  const next = nextPuzzleName();
  if (next) startLevel(next);
  else goToLevels();
}

function retryLevel() {
  // Replay the very same puzzle: rewind cubes to their snapshotted scrambled
  // start (same positions + orientations), restore the cursor and move budget,
  // rather than generating a fresh scramble. Abandon any open attempt first.
  finalizeAttempt("abandoned");
  hideOverlay();
  // Kill any in-flight roll animation NOW: if it survived the rewind it would
  // complete after restoreInitial(), snapping its cube to the pre-retry cell and
  // firing afterMove() on the fresh attempt.
  game.anim = null;
  if (game.initial.length === 0) { buildLevel(game.puzzleConfig); }
  else {
    restoreInitial();
    game.moves = game.startMoves;
    game.movesUsed = 0;
    game.userMoves = []; // replaying the same board ⇒ start recording afresh
    // Same opening cursor as a fresh build (the generator's cursorIndex — NOT
    // solution[0].cube, which tryImproveWithSolver may have replaced with a
    // different opening cube; the server verifies wins from the former).
    game.selected = game.initialSelected;
    game.solving = false;
    game.solveQueue = [];
    game.walk = null;
    updateCursor(true);
    updateHud();
  }
  game.state = "playing";
  syncWinColorPicker();
  beginAttempt();
}

function overlayAction() {
  if (game.state === "won") nextLevel();
  else retryLevel(); // lost, or end-of-solution
}

// Return to the landing page (the level picker lives there now).
function goToLevels() {
  finalizeAttempt("abandoned");
  location.href = "index.html";
}

