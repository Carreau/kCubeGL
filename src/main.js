/* --- Backend attempt tracking ----------------------------------------------
 * Every time a player starts a board we open an "attempt" on the server and
 * close it with an outcome (won / lost / abandoned). These rows are what later
 * powers skill and difficulty analysis. All calls are best-effort: if the server
 * is briefly unreachable they quietly no-op and the game keeps running.
 * ------------------------------------------------------------------------- */

// Resolve the stored token to a username (and Gravatar) for the HUD.
async function refreshIdentity() {
  const who = await api.me();
  game.username = who && who.username ? who.username : null;
  if (el.user) el.user.textContent = game.username ? "@" + game.username : "guest";
  if (el.userAvatar) {
    if (game.username) {
      // A linked email gives a real Gravatar; otherwise hash the username so
      // every signed-in player still gets a stable generated icon.
      el.userAvatar.src = who.avatarHash
        ? gravatarUrlForHash(who.avatarHash, { size: 36 })
        : gravatarUrl(game.username, { size: 36 });
      el.userAvatar.hidden = false;
    } else {
      el.userAvatar.hidden = true;
    }
  }
}

// Open a new attempt for the current board and refresh the world-record badge.
// `puzzleInfo` is the already-fetched GET /api/puzzles/:name response from
// startLevel; passing it avoids a duplicate request. When null (e.g. retryLevel),
// a fresh fetch is made instead.
function beginAttempt(puzzleInfo = null) {
  game.attemptStart = performance.now();
  game.attemptId = null;
  // Staleness guard: if the player switches boards before this response lands,
  // the old attempt's id must not be installed onto the new board. Capture the
  // generation now and ignore (best-effort abandon) any stale resolution.
  const gen = ++game.attemptGen;
  api.startAttempt({ puzzle: game.puzzleName }).then((r) => {
    if (!r || !r.attemptId) return;
    if (gen !== game.attemptGen) {
      // A newer board took over while this request was in flight — close the
      // orphaned attempt server-side so it doesn't linger open forever.
      api.finishAttempt(r.attemptId, { outcome: "abandoned", movesUsed: 0, durationMs: 0, moveSeq: "" });
      return;
    }
    game.attemptId = r.attemptId;
  });

  // Clear the previous puzzle's world-best badge immediately: if the server is
  // unreachable the fetch below resolves null and the stale value would linger.
  if (el.world) el.world.textContent = "–";
  if (puzzleInfo) {
    if (el.world) el.world.textContent = puzzleInfo.worldBest == null ? "–" : puzzleInfo.worldBest;
  } else {
    api.getPuzzle(game.puzzleName).then((info) => {
      if (gen !== game.attemptGen) return; // board changed meanwhile — stale badge
      if (info && el.world) el.world.textContent = info.worldBest == null ? "–" : info.worldBest;
    });
  }
}

// Close the open attempt with an outcome. No-op if none is open (e.g. already
// finalised by a win, or offline). Uses the CURRENT movesUsed/elapsed time, so
// call it before rebuilding the board on retry/abandon.
function finalizeAttempt(outcome) {
  if (!game.attemptId) return;
  const id = game.attemptId;
  game.attemptId = null;
  const durationMs = Math.round(performance.now() - game.attemptStart);
  const moveSeq = game.userMoves.join(""); // the player's full cursor path, R/L/U/D
  api.finishAttempt(id, { outcome, movesUsed: game.movesUsed, durationMs, moveSeq }).then((r) => {
    if (outcome === "won" && r && el.world) {
      el.world.textContent = r.worldBest == null ? "–" : r.worldBest;
    }
  });
}

// Keep the address bar in step with the puzzle being played, so a refresh or a
// shared link lands on the same puzzle.
function syncUrl(name) {
  try {
    const u = new URL(location.href);
    u.searchParams.set("puzzle", name);
    history.replaceState(null, "", u);
  } catch { /* ignore */ }
}

