function updateCursor(snap) {
  const cube = game.cubes[game.selected];
  if (!cube) return;
  const tx = cube.mesh.position.x;
  const tz = cube.mesh.position.z;
  if (snap) {
    cursor.position.set(tx, S + 0.55, tz);
  } else {
    cursor.position.x += (tx - cursor.position.x) * 0.4;
    cursor.position.z += (tz - cursor.position.z) * 0.4;
  }
}

