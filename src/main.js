// Are the current cubes a single 4-connected (N/S/E/W) block? (cellsConnected
// lives in shared.mjs, the shared source of truth.)
function isContiguous() {
  return cellsConnected(game.cubes.map((c) => [c.r, c.c]));
}

/* --- Level generation ------------------------------------------------------
 * The board itself is built by generateLevel() in src/level-gen.mjs: start from
 * a solved board (all cubes the target colour up, in one contiguous block), then
 * apply N random reverse-rolls, each from the current cursor's island so the
 * reversed sequence is a route the cursor can actually walk back. buildLevel()
 * below just renders that result and records the reverse as the solution.
 * ------------------------------------------------------------------------- */

// Shortest route of cubes from `from` to `to`, stepping only between cubes that
// are 4-neighbours (the moves a cursor can make). BFS over the island graph;
// returns the cube path inclusive of both ends, or null if `to` isn't reachable
// (shouldn't happen — the scramble keeps every solution move inside one island).
function cursorPath(from, to) {
  if (from === to) return [from];
  const prev = new Map();
  const seen = new Set([from]);
  const queue = [from];
  for (let i = 0; i < queue.length; i++) {
    const c = queue[i];
    if (c === to) break;
    for (const [dr, dc] of NEI) {
      const n = cubeAt(game.cubes, c.r + dr, c.c + dc);
      if (n && !seen.has(n)) { seen.add(n); prev.set(n, c); queue.push(n); }
    }
  }
  if (!seen.has(to)) return null;
  const path = [to];
  for (let c = to; c !== from; ) { c = prev.get(c); path.push(c); }
  return path.reverse();
}

function buildLevel(config) {
  // Generate the scrambled board from the puzzle's seed (pure, shared with the
  // server, so it's the identical board on every device). config comes from the
  // server, with the deterministic catalogue as a cold-start cache when it's
  // briefly unreachable.
  const gen = generateLevel(config);
  game.targetColor = gen.targetColor;

  // Render the generated state: a cube per cell, oriented by the quaternion the
  // generator produced (lifted from a plain [x,y,z,w] array into THREE here).
  for (const c of game.cubes) c.dispose();
  game.cubes = gen.cubes.map((g) => {
    const cube = new Cube(g.r, g.c);
    cube.mesh.quaternion.fromArray(g.quat);
    return cube;
  });

  // Solution = scramble reversed, each roll undone in the opposite direction —
  // a route back through the same states to the contiguous, uniform start.
  game.solution = gen.scramble
    .slice().reverse()
    .map(({ cubeIndex, key }) => ({ cube: game.cubes[cubeIndex], key: OPPOSITE[key] }));

  // Snapshot the scrambled start so "show solution" / Retry can replay from here.
  game.initial = game.cubes.map((k) => ({
    cube: k, r: k.r, c: k.c, quat: k.mesh.quaternion.clone(),
  }));

  // Move budget. `par` is the bonus-free budget (the level's par, reported to the
  // backend); `optimal` is the shortest known solve for this board (its solution
  // length). Par stays generously above the raw scramble count to leave room for
  // exploratory play (cursor hops within an island are free).
  game.par = config.par;
  game.optimal = game.solution.length;
  game.moves = game.par + game.bonus;
  game.startMoves = game.moves; // remember the budget so Retry can restore it
  game.movesUsed = 0;
  game.userMoves = []; // fresh board ⇒ fresh recording of the player's cursor path
  // Start the cursor on the last-scrambled cube (solution[0].cube) so par is
  // reachable from the opening position, not just by "show solution".
  game.selected = gen.cursorIndex;
  // Remember it: Retry must restore THIS cursor, not the (possibly
  // solver-improved) solution's first cube — the server replays recorded wins
  // from the generator's opening cursor, so a different start would make a
  // legitimate retry win unverifiable.
  game.initialSelected = gen.cursorIndex;
  game.solving = false;
  game.solveQueue = [];
  game.walk = null;
  game.anim = null; // kill any in-flight roll so it can't land on the fresh board

  updateCursor(true);
  updateHud();
}

/* --- Win / lose ------------------------------------------------------------- */

function isUniform() {
  if (game.cubes.length === 0) return false;
  const target = game.winColor ?? game.cubes[0].topColor;
  return game.cubes.every((c) => c.topColor === target);
}

// Solved = same colour up on every cube AND a single connected block.
function isSolved() {
  return isUniform() && isContiguous();
}

/* --- Input / rolling -------------------------------------------------------- */

function selectCube(idx) {
  game.selected = idx;
  const cube = game.cubes[idx];
  if (!cube) return;
  // Slide from the cursor's current visual position to the new cube.
  game.walk = {
    pts: [{ x: cursor.position.x, z: cursor.position.z },
          { x: cellX(cube.c), z: cellZ(cube.r) }],
    seg: 0, t: 0, onDone: null, ms: CURSOR_HOP_MS,
  };
}

function tryMove(key) {
  if (game.state !== "playing" || game.anim) return;
  const dir = DIRS[key];
  if (!dir) return;
  const cube = game.cubes[game.selected];
  const nr = cube.r + dir.dr, nc = cube.c + dir.dc;
  if (!inBounds(nr, nc)) return; // edge: nothing happens

  const other = cubeAt(game.cubes, nr, nc);
  if (other) {
    // re-select neighbour — free, no move spent. Still a cursor move, so record it
    // (the recorded R/L/U/D stream replays the player's full path, not just rolls).
    game.userMoves.push(KEY_CODE[key]);
    selectCube(game.cubes.indexOf(other));
    return;
  }

  if (game.moves <= 0) return;
  game.userMoves.push(KEY_CODE[key]); // a roll: the cursor rides the cube it tips
  startRoll(cube, dir, nr, nc, true);
}

// cost=false rolls are free (used when auto-playing the solution).
function startRoll(cube, dir, nr, nc, cost) {
  game.walk = null; // cancel any in-progress cursor slide
  const center = cube.mesh.position.clone();
  const pivot = center.clone().add(dir.edgeOffset);
  game.anim = {
    cube, dir, nr, nc, pivot,
    startPos: center.clone().sub(pivot), // position relative to pivot
    startQuat: cube.mesh.quaternion.clone(),
    t: 0,
  };
  if (cost) {
    game.moves -= 1;
    game.movesUsed += 1;
  }
  updateHud();
}

function stepRoll(dt) {
  const a = game.anim;
  a.t = Math.min(1, a.t + dt / (game.solving ? SOLVE_ROLL_MS : ROLL_MS));
  const e = easeInOut(a.t); // for a satisfying tip
  const angle = a.dir.angle * e;
  const q = new THREE.Quaternion().setFromAxisAngle(a.dir.axis, angle);
  a.cube.mesh.position.copy(a.startPos).applyQuaternion(q).add(a.pivot);
  a.cube.mesh.quaternion.copy(a.startQuat).premultiply(q);

  if (a.t >= 1) {
    // snap to final logical state
    const final = new THREE.Quaternion().setFromAxisAngle(a.dir.axis, a.dir.angle);
    a.cube.mesh.quaternion.copy(a.startQuat).premultiply(final);
    a.cube.setCell(a.nr, a.nc);
    a.cube.syncMesh();
    game.anim = null;
    updateCursor(false);
    if (game.solving) setTimeout(solutionStep, SOLVE_STEP_MS); // slowed pause between moves
    else afterMove();
  } else {
    updateCursor(false);
  }
}

// Glide the auto-solve cursor along a path of cubes (from cursorPath), easing
// between each adjacent pair, then run `onDone` (the actual roll). This replaces
// the old teleport when consecutive solution moves sit on different cubes.
function startCursorWalk(path, onDone) {
  const pts = path.map((c) => ({ x: cellX(c.c), z: cellZ(c.r) }));
  game.walk = { pts, seg: 0, t: 0, onDone };
}

function stepWalk(dt) {
  const w = game.walk;
  w.t += dt / (w.ms ?? SOLVE_CURSOR_MS);
  // carry the residual into later segments if a frame spanned more than one
  while (w.t >= 1 && w.seg < w.pts.length - 2) { w.t -= 1; w.seg += 1; }
  const onLast = w.seg >= w.pts.length - 2;
  const s = onLast ? Math.min(w.t, 1) : w.t;
  const a = w.pts[w.seg], b = w.pts[w.seg + 1];
  const e = easeInOut(s); // ease per step
  cursor.position.x = a.x + (b.x - a.x) * e;
  cursor.position.z = a.z + (b.z - a.z) * e;
  if (onLast && w.t >= 1) {
    const done = w.onDone;
    game.walk = null;
    if (done) done();
  }
}

function afterMove() {
  if (isSolved()) {
    game.state = "won";
    game.bonus += 1; // clearing a puzzle grants +1 carried move
    finalizeAttempt("won"); // close the attempt as a win (records moves + time + path)
    const { isRecord, best, prev } = recordScore(game.puzzleName, game.movesUsed);
    const scoreLine = isRecord
      ? (prev === undefined
          ? `Solved in <b>${game.movesUsed}</b> moves.`
          : `Solved in <b>${game.movesUsed}</b> moves — <b>new best!</b> (was ${prev})`)
      : `Solved in <b>${game.movesUsed}</b> moves (best ${best}).`;
    const hasNext = nextPuzzleName() != null;
    showOverlay(
      "Solved!",
      `${scoreLine}<br><b>${esc(game.puzzleName)}</b> cleared. <b>+1</b> bonus move banked (carried: ${game.bonus}).`,
      hasNext ? "Next puzzle" : "Back to puzzles"
    );
    updateHud();
    syncWinColorPicker();
  } else if (game.moves <= 0) {
    game.state = "lost";
    finalizeAttempt("lost"); // close the attempt as a loss (ran out of moves)
    const colourHint = game.winColor !== null
      ? `Get all cubes showing <strong>${COLORS[game.winColor].name}</strong> on top in one connected block.`
      : "Not quite — every cube the same colour in one connected block.";
    showOverlay(
      "Out of moves",
      `${colourHint} Press <b>S</b> to see a solution, or retry.`,
      "Retry"
    );
    syncWinColorPicker();
  }
}

/* --- Show solution ---------------------------------------------------------- */

// Build the pure solver state from the stored initial snapshot so the solver
// always starts from the scrambled board regardless of what the player has done.
// quatToFaces (from level-gen) takes a plain [x,y,z,w] array; faces[2] is the top.
function extractInitialSolverState() {
  const firstCube = game.solution.length ? game.solution[0].cube : game.cubes[0];
  const cursorId = game.cubes.indexOf(firstCube);
  return {
    cubes: game.initial.map((snap, i) => ({
      id: i,
      r: snap.r,
      c: snap.c,
      faces: quatToFaces(snap.quat.toArray()),
    })),
    cursorId,
  };
}

// Convert solver output [{id, dir}] to the game solution format [{cube, key}].
function solverSolutionToGame(solverMoves) {
  return solverMoves.map(m => ({ cube: game.cubes[m.id], key: m.dir }));
}

// Try BFS then greedy to find a solution at least as good as the stored one.
// Updates game.solution in place if a shorter (or equally short) path is found.
// BFS gives the provably optimal roll count; greedy is a fast fallback.
function tryImproveWithSolver() {
  if (!game.initial.length) return;
  const solverState = extractInitialSolverState();

  // BFS explores at most BFS_MAX_DEPTH moves: when the stored solution is
  // already longer, BFS provably cannot improve on it — and on hard boards it
  // would freeze the UI for seconds before returning null. Skip it entirely.
  const BFS_MAX_DEPTH = 20;
  if (game.solution.length <= BFS_MAX_DEPTH) {
    const bfs = bfsSolve(solverState, { maxDepth: BFS_MAX_DEPTH, targetColor: game.winColor });
    if (bfs && bfs.length <= game.solution.length) {
      game.solution = solverSolutionToGame(bfs);
      return;
    }
  }

  // Pass the player's chosen win colour: without it greedy can "improve" the
  // solution into one that solves to the wrong uniform colour.
  const greedy = greedySolve(solverState, { targetColor: game.winColor });
  if (greedy && greedy.length < game.solution.length) {
    game.solution = solverSolutionToGame(greedy);
  }
}

function restoreInitial() {
  for (const s of game.initial) {
    s.cube.setCell(s.r, s.c);
    s.cube.mesh.quaternion.copy(s.quat);
    s.cube.syncMesh();
  }
}

function showSolution() {
  if (game.anim || game.solving) return;
  if (game.state !== "playing" && game.state !== "lost") return;
  if (game.solution.length === 0) return;
  // Watching the solution forfeits the run: close the open attempt as abandoned
  // (lost/won states have already been finalised; finalizeAttempt no-ops then).
  if (game.state === "playing") finalizeAttempt("abandoned");
  tryImproveWithSolver(); // replace game.solution if BFS/greedy finds a shorter path
  hideOverlay();
  restoreInitial(); // rewind to the scrambled start
  game.solving = true;
  game.state = "solving";
  game.solveQueue = game.solution.slice();
  // Begin on the first cube the solution rolls — the same spot a player starts —
  // so the opening step is trivially in-island (matches the player's cursor too).
  game.selected = game.cubes.indexOf(game.solveQueue[0].cube);
  updateCursor(true);
  updateHud();
  solutionStep();
}

function solutionStep() {
  if (!game.solving) return; // stale step (e.g. a pending timer after exit) — ignore
  if (game.solveQueue.length === 0) {
    game.solving = false;
    game.state = "lost"; // not a legitimate clear — offer a retry
    showOverlay(
      "Solution",
      "That's one way: every cube the same colour in one connected block. Press <b>Retry</b> to attempt this same puzzle yourself.",
      "Retry"
    );
    return;
  }
  const { cube, key } = game.solveQueue.shift();
  const from = game.cubes[game.selected];

  // The cursor rides the cube it rolls, so it must first travel to that cube.
  // Walk it there cube-by-cube along the island instead of teleporting; once it
  // arrives, perform the actual roll.
  const roll = () => {
    game.selected = game.cubes.indexOf(cube);
    updateCursor(true);
    const dir = DIRS[key];
    startRoll(cube, dir, cube.r + dir.dr, cube.c + dir.dc, false);
  };
  const path = cursorPath(from, cube);
  if (path && path.length > 1) startCursorWalk(path, roll);
  else roll();
}

