// The 3D-face hex for a colour in a theme: the light theme overrides the shared
// COLORS with a punchier set; HUD swatches keep using COLORS everywhere.
function faceHex(colorIdx, t) {
  return (t.faces && t.faces[COLORS[colorIdx].name]) ?? COLORS[colorIdx].hex;
}

// Build a canvas texture: a coloured rounded square on a themed background, so
// faces read clearly and adjacent cubes still show a seam between them.
function faceTexture(hex, bg = SCENE_THEME.dark.faceBg, sheen = SCENE_THEME.dark.sheen) {
  const size = 128;
  const cv = document.createElement("canvas");
  cv.width = cv.height = size;
  const ctx = cv.getContext("2d");
  ctx.fillStyle = bg;
  ctx.fillRect(0, 0, size, size);
  const pad = 12, r = 22, w = size - pad * 2;
  const col = "#" + hex.toString(16).padStart(6, "0");
  ctx.fillStyle = col;
  ctx.beginPath();
  ctx.roundRect(pad, pad, w, w, r);
  ctx.fill();
  // subtle top sheen
  const grad = ctx.createLinearGradient(0, pad, 0, pad + w);
  grad.addColorStop(0, `rgba(255,255,255,${sheen})`);
  grad.addColorStop(0.5, "rgba(255,255,255,0)");
  ctx.fillStyle = grad;
  ctx.beginPath();
  ctx.roundRect(pad, pad, w, w, r);
  ctx.fill();
  const tex = new THREE.CanvasTexture(cv);
  tex.anisotropy = 4;
  tex.colorSpace = THREE.SRGBColorSpace;
  return tex;
}

// BoxGeometry material order is: +X, -X, +Y, -Y, +Z, -Z — matching FACE_AXES.
// All cubes share this material array, so rebuilding each map on a theme change
// (see applySceneTheme) recolours every cube's face seams at once.
const FACE_MATERIALS = FACE_AXES.map(
  (f) => {
    const t = SCENE_THEME[initTheme()] || SCENE_THEME.dark;
    return new THREE.MeshStandardMaterial({
      map: faceTexture(faceHex(f.color, t), t.faceBg, t.sheen),
      roughness: 0.55,
      metalness: 0.05,
    });
  }
);

// Which colour faces up for a given orientation quaternion. quatToFaces returns
// the [+X,-X,+Y,-Y,+Z,-Z] colour slots; index 2 is the +Y (top) face.
function topColor(quat) {
  return quatToFaces(quat.toArray())[2];
}

/* A lightly bevelled cube. RoundedBoxGeometry is single-material, so we rebuild
 * the per-face material groups (BoxGeometry order: +X,-X,+Y,-Y,+Z,-Z) by
 * classifying each triangle to the face its centroid points toward. The bevel
 * is slight, so colouring the thin rounded edges by nearest face reads cleanly. */
function bevelledCubeGeometry(size, radius, segments) {
  // RoundedBoxGeometry is non-indexed: every 3 consecutive vertices form one
  // triangle. Classify each triangle's centroid to a face and emit a run of
  // material groups so the 6 face textures map onto the bevelled box.
  const geo = new RoundedBoxGeometry(size, size, size, segments, radius);
  const pos = geo.attributes.position;
  const faceOf = (x, y, z) => {
    const ax = Math.abs(x), ay = Math.abs(y), az = Math.abs(z);
    if (ax >= ay && ax >= az) return x >= 0 ? 0 : 1;
    if (ay >= az) return y >= 0 ? 2 : 3;
    return z >= 0 ? 4 : 5;
  };
  geo.clearGroups();
  const tris = pos.count / 3;
  let runStart = 0, runMat = -1;
  for (let t = 0; t < tris; t++) {
    const i = t * 3;
    const x = (pos.getX(i) + pos.getX(i + 1) + pos.getX(i + 2)) / 3;
    const y = (pos.getY(i) + pos.getY(i + 1) + pos.getY(i + 2)) / 3;
    const z = (pos.getZ(i) + pos.getZ(i + 1) + pos.getZ(i + 2)) / 3;
    const m = faceOf(x, y, z);
    if (m !== runMat) {
      if (runMat !== -1) geo.addGroup(runStart * 3, (t - runStart) * 3, runMat);
      runStart = t; runMat = m;
    }
  }
  geo.addGroup(runStart * 3, (tris - runStart) * 3, runMat);
  return geo;
}

/* --- Persisted scores ------------------------------------------------------- */

function loadStore() {
  try { return JSON.parse(localStorage.getItem(STORE_KEY)) || {}; }
  catch { return {}; }
}
function saveStore(s) {
  try { localStorage.setItem(STORE_KEY, JSON.stringify(s)); } catch { /* ignore */ }
}
function bestFor(name) {
  const b = loadStore().best;
  return b && b[name] !== undefined ? b[name] : null;
}
// Record a clear: keep the fewest moves ever used to solve this puzzle (by name).
function recordScore(name, movesUsed) {
  const s = loadStore();
  s.best = s.best || {};
  s.cleared = s.cleared || {};
  const prev = s.best[name];
  const isRecord = prev === undefined || movesUsed < prev;
  if (isRecord) s.best[name] = movesUsed;
  s.cleared[name] = (s.cleared[name] || 0) + 1;
  saveStore(s);
  return { isRecord, best: s.best[name], prev };
}

