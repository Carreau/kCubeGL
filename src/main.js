let last = performance.now();
function loop(now) {
  const dt = Math.min(now - last, 50);
  last = now;

  if (game.anim) stepRoll(dt);
  else if (game.walk) stepWalk(dt);

  // ease the camera toward its target yaw
  if (Math.abs(camYawTarget - camYaw) > 1e-4) {
    camYaw += (camYawTarget - camYaw) * 0.15;
    applyCamera();
  }

  // cursor bobs; tip snaps to the same 90° grid as the arrow-key remapping so it
  // always points toward where ArrowUp actually sends the cube.
  cursor.position.y = S + 0.55 + Math.sin(now * 0.004) * 0.07;
  cursor.rotation.y = camQuarterTurns() * (Math.PI / 2);

  renderer.render(scene, camera);
  requestAnimationFrame(loop);
}

// Load the ordered list of puzzle names (for "Next puzzle"). Uses the server's
// order (pinned first) when online, falling back to the deterministic catalogue.
async function loadOrder() {
  const list = await api.listPuzzles();
  game.order = Array.isArray(list) && list.length
    ? list.map((p) => p.name)
    : buildCatalog().map((p) => p.name);
}

