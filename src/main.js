/* ============================================================================
 * kCube — a 3D dice-rolling puzzle.
 *
 * Roll cubes around a 5×5 board until every cube shows the same colour on top
 * AND they form one connected (N/S/E/W) block, within a limited move budget.
 * Levels are generated by scrambling a solved board with random reverse-rolls,
 * which guarantees solvability; the exact reverse is stored so it can be played
 * back as a "show solution". Best scores are persisted in localStorage so any
 * level can be replayed to beat your record.
 * ========================================================================== */

/* --- Constants -------------------------------------------------------------- */

const S = 1.0; // cell spacing == cube size (so a roll lands exactly one cell over)
const HALF = S / 2;
const ROLL_MS = 170; // duration of a roll animation
const STORE_KEY = "kcube.v1"; // localStorage key for persisted scores

// Solution-playback pacing (slower than live play so each step is watchable).
const SOLVE_ROLL_MS = 420; // roll duration while auto-solving (vs ROLL_MS)
const SOLVE_STEP_MS = 380; // pause between solution moves
const SOLVE_CURSOR_MS = 130; // per-cube step as the cursor walks between solution moves
const CURSOR_HOP_MS = 80;   // smooth slide when the player hops to an adjacent cube

// COLORS imported from ./shared.mjs (pure; also used by the server + landing page).

// Fixed mapping from a cube's LOCAL face axis -> colour id, lifted from the
// shared generation tables (FACE_AXES order = BoxGeometry material order). Every
// cube shares this mapping; only orientation differs.
const FACE_AXES = GEN_FACE_AXES.map((f) => ({ v: new THREE.Vector3(...f.v), color: f.color }));

// Arrow-key directions for live play + animation. dr/dc and the rotation
// (axis/angle) come from the shared generation tables so the physical tip-over
// matches the orientation the generator produced; edgeOffset (the contact edge,
// added to the cube CENTRE to find the pivot) is the only render-only extra.
const DIRS = Object.fromEntries(Object.entries(GEN_DIRS).map(([k, d]) => [k, {
  dr: d.dr, dc: d.dc,
  axis: new THREE.Vector3(...d.axis), angle: d.angle,
  edgeOffset: new THREE.Vector3(d.dc * HALF, -HALF, d.dr * HALF),
}]));

// Compact one-char code per arrow, used to record the player's cursor moves as a
// replayable string (R/L/U/D) for backend storage.
const KEY_CODE = {
  ArrowRight: "R", ArrowLeft: "L", ArrowUp: "U", ArrowDown: "D",
};

// Arrow-key order for remapping (CW: Up→Right→Down→Left).
const ARROW_CW = ["ArrowUp", "ArrowRight", "ArrowDown", "ArrowLeft"];

// When the camera has orbited, remap a physical arrow key to the board direction
// that matches the player's visual "forward/right/back/left". Uses camYawTarget
// so the mapping snaps the moment Q/E is pressed, before the camera animation
// finishes. A quarter-turn CW rotation of the camera shifts the logical key one
// step CCW in the [Up, Right, Down, Left] cycle.
function remapArrowKey(key) {
  const idx = ARROW_CW.indexOf(key);
  if (idx === -1) return key;
  const rot = (camQuarterTurns() % 4 + 4) % 4;
  return ARROW_CW[(idx + 4 - rot) % 4];
}

// How many quarter-turns the camera is heading toward (from camYawTarget, so it
// snaps the moment Q/E is pressed). Shared by the arrow-key remap above and the
// cursor-tip orientation in the render loop, which must agree on the same grid.
function camQuarterTurns() {
  return Math.round(camYawTarget / (Math.PI / 2));
}

